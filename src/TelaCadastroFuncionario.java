    private Cargo PegarCargo(){
        
        int indiceCargo = cbCargo.getSelectedIndex();
        
        if(indiceCargo == 1){
            return Cargo.GERENTE;
        }else{
            return Cargo.FUNCIONARIO;
        }
    }
    
    private void btCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btCadastrarActionPerformed
        if(tfNome.getText().equals("")||tfCelular.getText().equals("")||tfCpf.getText().equals("")||tfEmail.getText().equals("")||pfSenha.getText().equals("")||pfCSenha.getText().equals("")||cbCargo.getSelectedIndex()==0){
            JOptionPane.showMessageDialog(rootPane, "Preencha todos os campos!");
        }else{
            if(pfSenha.getText().equals(pfCSenha.getText())){
                if(!alterar){
                    Funcionario user = new Funcionario (PegarCargo(),tfNome.getText(), tfCelular.getText(), tfCpf.getText(), tfEmail.getText(), pfSenha.getText());
                    funcionarios.add(user);// Adciona na ArrayList
                    try {
                        Gravador.gravar("funcionarios.data", funcionarios);
                    } catch (IOException ex) {
                        Logger.getLogger(TelaCadastroFuncionario.class.getName()).log(Level.SEVERE, null, ex);
                    }
                    JOptionPane.showMessageDialog(rootPane, "Usuário cadastrado");
                    dispose();
                    Login login = new Login();
                    login.setVisible(true);
                    limparTela();
                }else{
                    Funcionario user = new Funcionario(PegarCargo(),tfNome.getText(), tfCelular.getText(), tfCpf.getText(), tfEmail.getText(), pfSenha.getText());
                    funcionarios.set(id, user);// Altera no ArrayList

                    try {
                        Gravador.gravar("funcionarios.data", funcionarios);
                    } catch (IOException ex) {
                        Logger.getLogger(TelaCadastroFuncionario.class.getName()).log(Level.SEVERE, null, ex);
                    }
                    Listagem Listagem = new Listagem();
                    Listagem.setVisible(true);
                    dispose();
                    JOptionPane.showMessageDialog(rootPane, "Usuário alterado");
                }
            }else{
                JOptionPane.showMessageDialog(rootPane, "Senhas não são iguais.");
            }
        }
    }//GEN-LAST:event_btCadastrarActionPerformed

