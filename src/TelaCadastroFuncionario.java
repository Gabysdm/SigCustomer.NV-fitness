    public void mostrarDados() {
        if(id >= 0){
            String nome = funcionarios.get(id).getNome(); //Pega o nome do usuario
            String telefone = funcionarios.get(id).getCelular();
            String email = funcionarios.get(id).getEmail();
            String cpf = funcionarios.get(id).getCpf();
            String senha = funcionarios.get(id).getSenha();
            
            //preenche os dados no formulario
            tfNome.setText(nome);
            tfCelular.setText(telefone);
            tfEmail.setText(email);
            tfCpf.setText(cpf);
            pfSenha.setText(senha);
            pfCSenha.setText(senha);
            
            btCadastrar.setText("Alterar");
        }
          
    }
    
